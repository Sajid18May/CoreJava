package operators;
//Length and breadth of a rectangle are 5 and 7 respectively. Write a program to calculate the area and perimeter of the rectangle.

public class prb_4 {

	public static void main(String[] args) {
		int Length=5;
		int breadth=7;
		System.out.println("Area of Rectangle = "+breadth*Length);
		System.out.println("Area of Perimeter = "+2*(breadth+Length));

	}

}
